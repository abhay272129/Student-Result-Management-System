    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        String rollNo=rollNumber.getText();
        String Dbms=dbms.getText();
        String Cn=cn.getText();
        String Nm=nm.getText();
        String Os=os.getText();
        String Math=math.getText();
        
              try{
                 Class.forName("com.mysql.cj.jdbc.Driver"); //calling mysql driver class
                 //making connection with database by creating the object of connection class 
                 Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","Abhay272129@");
                 
                 Statement st=conn.createStatement();
                 // if entered roll number is already present in student table then i will add the result soo for this i have to execute this query
                 ResultSet rs=st.executeQuery("select * from student where rollNO='"+rollNo+"'");
                 //here rs is always pointing to null for this situation i will check rs.next() and if it is true then i will add the data 
                 if(rs.next()){
                     st.executeUpdate("insert into result(rollNO,DBMS,CN,NM,OS,Math) values('"+rollNo+"','"+Dbms+"','"+Cn+"','"+Nm+"','"+Os+"','"+Math+"')");
                     JOptionPane.showMessageDialog(null,"Result added Successfully");
                 }
                 else 
                  JOptionPane.showMessageDialog(null,"Student is not Registered");
                  setVisible(false);
                  new insertResult().setVisible(true);
              }
              catch(Exception e){
                  JOptionPane.showMessageDialog(null,e.toString());
                  
              }
        // TODO add your handling code here:
    }//GEN-LAST:event_saveActionPerformed

